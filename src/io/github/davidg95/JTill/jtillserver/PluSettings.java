    public PluSettings(DataConnect dc, Image icon) {
        this.dc = dc;
        initComponents();
        setIconImage(icon);
        setTitle("PLU Settings");
        try {
            txtUPC.setText(dc.getSetting("UPC_PREFIX"));
            txtLength.setText(dc.getSetting("BARCODE_LENGTH"));

            String upc = dc.getSetting("UPC_PREFIX");
            int length = Integer.parseInt(dc.getSetting("BARCODE_LENGTH"));
            if (!upc.equals("")) {
                int lengthToAdd = length - upc.length();
                String ref = dc.getSetting("NEXT_PLU");
                for (int i = 1; i < lengthToAdd; i++) {
                    ref = 0 + ref;
                }
                String barcode = upc + ref;
                txtNext.setText(barcode);
            }

        } catch (IOException ex) {
            Logger.getLogger(PluSettings.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public static void showWindow(DataConnect dc, Image icon) {
        new PluSettings(dc, icon).setVisible(true);
    }

    private void saveUPC() {
        String upc = txtUPC.getText();

        if (upc.equals("")) {
            JOptionPane.showMessageDialog(this, "If you do not specify a prefix, you will not be able to generate any barcodes", "UPC Prefix", JOptionPane.WARNING_MESSAGE);
        } else if (upc.length() < 6 || upc.length() > 10) {
            JOptionPane.showMessageDialog(this, "UPC Prefix must be between 6 and 10 characters long", "UPC Prefix", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (!upc.matches("[0-9]+")) {
            JOptionPane.showMessageDialog(this, "Must only contain numbers", "UPC Prefix", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            dc.setSetting("UPC_PREFIX", upc);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex, "UPC Prefix", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void saveLength() {
        if (txtLength.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "You must sepcify a length", "Length", JOptionPane.WARNING_MESSAGE);
        } else if (!txtLength.getText().matches("[0-9]+")) {
            JOptionPane.showMessageDialog(this, "Must only contain numbers", "Length", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int l = Integer.parseInt(txtLength.getText());
        if (l < 8 || l > 15) {
            JOptionPane.showMessageDialog(this, "Length must be between 8 and 15", "Length", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String length = txtLength.getText();

        try {
            dc.setSetting("BARCODE_LENGTH", length);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex, "Barcode Length", JOptionPane.ERROR_MESSAGE);
        }
    }

