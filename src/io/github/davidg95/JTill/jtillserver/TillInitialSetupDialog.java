    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        try {
            if (txtName.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Must enter a terminal name", "Terminal Setup", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String name = txtName.getText();
            if (jtill.getDataConnection().isTillNameUsed(name)) {
                JOptionPane.showMessageDialog(this, "That name is already in use", "Terminal Setup", JOptionPane.ERROR_MESSAGE);
                return;
            }
            till.setName(txtName.getText());
            Screen s = (Screen) model.getSelectedItem();
            till.setDefaultScreen(s.getId());
            this.setVisible(false);
        } catch (IOException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

