    private void init() {
        try {
            model = new MyModel(jtill.getDataConnection().getAllScreens());
            cmbScreen.setModel(model);
            txtName.setText(till.getName());
        } catch (IOException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    public static Till showDialog(JTill jtill, Component parent, Till t) {
        Window window = null;
        if (parent instanceof Dialog || parent instanceof Frame) {
            window = (Window) parent;
        }
        till = t;
        final TillInitialSetupDialog dialog = new TillInitialSetupDialog(jtill, window);
        dialog.setVisible(true);
        return till;
    }

    private class MyModel implements ComboBoxModel {

        private final List<Screen> screens;
        private Screen selected;

        public MyModel(List<Screen> screens) {
            this.screens = screens;
            if (!screens.isEmpty()) {
                selected = screens.get(0);
            } else {
                selected = null;
            }
        }

        @Override
        public void setSelectedItem(Object anItem) {
            selected = (Screen) anItem;
        }

        @Override
        public Object getSelectedItem() {
            return selected;
        }

        @Override
        public int getSize() {
            return screens.size();
        }

        @Override
        public Object getElementAt(int index) {
            return screens.get(index);
        }

        @Override
        public void addListDataListener(ListDataListener l) {
        }

        @Override
        public void removeListDataListener(ListDataListener l) {
        }

    }

