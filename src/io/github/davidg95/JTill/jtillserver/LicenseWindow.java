    private void txtSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSubmitActionPerformed
        String name = txtName.getText();
        String key = txtKey.getText();
        File f = new File("license.dat");
        try {
            f.createNewFile();
            PrintWriter out = new PrintWriter(f);
            out.write(name + "\n");
            out.write(key);
            out.close();
            JOptionPane.showMessageDialog(this, "License set", "License", JOptionPane.INFORMATION_MESSAGE);
            setVisible(false);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtSubmitActionPerformed

