    public static void showWindow() {
        Product p = ProductSelectDialog.showDialog(GUI.gui);
        if (p == null) {
            return;
        }
        ProductEditWindow window = new ProductEditWindow(p);
        GUI.gui.internal.add(window);
        window.setVisible(true);
        try {
            window.setIcon(false);
            window.setSelected(true);
        } catch (PropertyVetoException ex) {
            Logger.getLogger(WasteStockWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void init() {
        try {
            List<Tax> taxes = dc.getAllTax();
            List<Category> categorys = dc.getAllCategorys();
            taxesModel = new DefaultComboBoxModel(taxes.toArray());
            categorysModel = new DefaultComboBoxModel(categorys.toArray());
            cmbTax.setModel(taxesModel);
            cmbCategory.setModel(categorysModel);
        } catch (SQLException | IOException ex) {
            JOptionPane.showMessageDialog(this, ex, "Error", 6);
        }
    }

    private void setProduct() {
        txtName.setText(p.getLongName());
        txtShortName.setText(p.getName());
        txtBarcode.setText(p.getBarcode());
        txtOrderCode.setText(p.getOrder_code() + "");
        categorysModel.setSelectedItem(p.getCategory());
        taxesModel.setSelectedItem(p.getTax());
        txtMinStock.setText(p.getMinStockLevel() + "");
        txtMaxStock.setText(p.getMaxStockLevel() + "");
        if (p.isOpen()) {
            radOpen.setSelected(true);
            for (Component c : panelStandard.getComponents()) {
                c.setEnabled(false);
            }
            for (Component c : panelOpen.getComponents()) {
                c.setEnabled(true);
            }
            txtPriceLimit.setText(p.getPriceLimit().toString());
            boolean scale = p.getScale() != -1;
            chkScale.setSelected(scale);
            lblScaleName.setEnabled(scale);
            lblScaleValue.setEnabled(scale);
            txtScaleName.setEnabled(scale);
            txtScaleValue.setEnabled(scale);
            if (scale) {
                txtScaleName.setText(p.getScaleName());
                txtScaleValue.setText(p.getScale() + "");
            }
        } else {
            radStandard.setSelected(true);
            for (Component c : panelStandard.getComponents()) {
                c.setEnabled(true);
            }
            for (Component c : panelOpen.getComponents()) {
                c.setEnabled(false);
            }
            txtPrice.setText(p.getPrice().toString());
            txtCostPrice.setText(p.getCostPrice().toString());
            txtPackSize.setText(p.getPackSize() + "");
            chkVat.setSelected(p.isPriceIncVat());
        }
    }

