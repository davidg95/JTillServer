    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        try {
            if (txtID.getText().isEmpty() || txtName.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Fill out all fields", "New Department", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (!Utilities.isNumber(txtID.getText())) {
                JOptionPane.showMessageDialog(this, "ID must be a number", "New Department", JOptionPane.ERROR_MESSAGE);
                return;
            }
            int id = Integer.parseInt(txtID.getText());
            if (jtill.getDataConnection().isDepartmentIDUsed(id)) {
                JOptionPane.showMessageDialog(this, "That ID is already in use", "New Department", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String name = txtName.getText();
            department = new Department(id, name);
            setVisible(false);
        } catch (IOException | SQLException ex) {
            jtill.getLogger().log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCreateActionPerformed

