    private void txtSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSearchActionPerformed
        try {
            int id = Integer.parseInt(txtID.getText());
            for (Supplier s : contents) {
                if (s.getId() == id) {
                    supplier = s;
                }
            }
            if (supplier == null) {
                JOptionPane.showMessageDialog(this, "Supplier not found", "Search", JOptionPane.ERROR_MESSAGE);
            } else {
                setVisible(false);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Must enter a number", "Search", JOptionPane.ERROR_MESSAGE);
        }
        txtID.setSelectionStart(0);
        txtID.setSelectionEnd(txtID.getText().length());
    }//GEN-LAST:event_txtSearchActionPerformed

